def checkio(labyrinth):
    sX,sY = 1,1
    eX,eY = 10,10
    paths = [(sX,sY,'')]
    visited = [(sX,sY)]
    while len(paths):
        x,y,p = paths.pop(0)
        if x==eX and y==eY:
            return p
        for i,j,s in [(x+1,y,'S'),(x-1,y,'N'),(x,y+1,'E'),(x,y-1,'W')]:
            if labyrinth[i][j] != 1 and (i,j) not in visited: # the borders are always 1
                visited.append((i,j))
                paths.append((i,j,p+s))
            


