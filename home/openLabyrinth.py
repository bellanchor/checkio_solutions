def listToRoute(l):
    res = ''
    for i in range(len(l)-1):
        l1, l2 = l[i], l[i+1]
        dx = l2[1] - l1[1]
        dy = l2[0] - l1[0]
        if (dy, dx) == (1, 0):
            res += 'S'
        elif (dy, dx) == (-1, 0):
            res += 'N'
        elif (dy, dx) == (0, 1):
            res += 'E'
        else:
            res += 'W'
    
    return res
            
    
def search(x, y, grid):
    path = [[x, y]]

    if (x, y) == (10,10):
    # print 'found at %d,%d' % (x, y)
        return True, path
    elif grid[x][y] == 1:
    # print 'wall at %d,%d' % (x, y)
        return False, 0
    elif grid[x][y] == 3:
        # print 'visited at %d,%d' % (x, y)
        return False, 0
    
    # print 'visiting %d,%d' % (x, y)

    # mark as visited
    grid[x][y] = 3

    if x < len(grid)-1:
        tmp = search(x+1, y, grid)    
        if tmp[0]:
            return True, path + tmp[1]
            
    if y > 0:
        tmp = search(x, y-1, grid)
        if tmp[0]:
            return True, path + tmp[1]
            
    if x > 0:
        tmp = search(x-1, y, grid)
        if tmp[0]:
            return True, path + tmp[1]
            
    if y < len(grid)-1:
        tmp = search(x, y+1, grid)
        if tmp[0]:
            return True, path + tmp[1]
        
    return False, 0


